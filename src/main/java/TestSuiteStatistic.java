public class TestSuiteStatistic {

    private int total;
    private int passed;
    private int pending;
    private int canceled;
    private int failed;
    private int broken;

    public int getTotal() {
        return total;
    }

    public int getPassed() {
        return passed;
    }

    public int getPending() {
        return pending;
    }

    public int getCanceled() {
        return canceled;
    }

    public int getFailed() {
        return failed;
    }

    public int getBroken() {
        return broken;
    }


}
